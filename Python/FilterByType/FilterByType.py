'''
Filter by Type '''

# Integer

sI = 45

if sI >= 100:
    print("That's a big number!")
else:
    print("that's a small number!")

# String

bS = "Tell me and I forget. Teach me and I remember. Involve me and I learn."

if len(bS) >= 50:
    print("Long sentence.")
else:
    print("Short sentence.")


# List

aL = [1,7,4,21]

if len(aL) >= 10:
    print("Big list.")
else:
    print("Short list.")